package pl.sk.webdav.document;

/**
 * Domain object for a stored document and its metadata.
 */
public class Document {
  private Long id;
  private String name;

  public Long getId() {
    return id;
  }

  public void setId(Long id) {
    this.id = id;
  }

  public String getName() {
    return name;
  }

  public void setName(String name) {
    this.name = name;
  }
}
